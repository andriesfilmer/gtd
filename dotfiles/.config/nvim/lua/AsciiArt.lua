local space = "      "

local combine = function(table1, table2, seperate)
    local result = {}
    for i = 1, 6 do
        result[i] = table1[i] .. (seperate and space or "") .. table2 [i]
    end

    return result
end

local num_to_ascii = function(number)
    if number < 10 then
        return numbers[number+1]
    end
    local ones = number % 10
    local tens = math.floor(number/10)

    return combine(numbers[tens+1], numbers[ones+1])
end

